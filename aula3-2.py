'''
+  -> adição
-  -> subtração
*  -> multiplicação
/  -> divisão
// -> divisão inteira
** -> exponenciação
%  -> resto da divisão
() -> modificador de prioridade
'''

print('Adição(+): ', 10+10)
print('Subtração(-): ', 10-5)
print('Multiplicação(*): ', 10*10)
print('Divisão(/): ', 10/2)
print('Exponenciação(**): ', 100**100)
print('Resto da Divisão(%): ', 3%2)
print('Modificador de Prioridade ( ): ', (10+20)*5)

